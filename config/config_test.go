	unsetEnv(t, "REVIEW_SUGGESTED_CHANGES")
	unsetEnv(t, "REVIEW_EVENTS")
	unsetEnv(t, "OVERVIEW")
	unsetEnv(t, "OVERVIEW_EVENTS")
	unsetEnv(t, "OVERVIEW_ISSUE_ALIGNMENT")
	unsetEnv(t, "REPLYCOMMENT")
	unsetEnv(t, "REPLYCOMMENT_EVENTS")
	unsetEnv(t, "REPLYCOMMENT_ACTIONS")
	unsetEnv(t, "AUTOGEN")
	unsetEnv(t, "AUTOGEN_EVENTS")
	unsetEnv(t, "AUTOGEN_DOCS")
	unsetEnv(t, "AUTOGEN_TESTS")

	tmp := t.TempDir()
	originalWD, err := os.Getwd()
	require.NoError(t, err)
	require.NoError(t, os.Chdir(tmp))
	t.Cleanup(func() {
		_ = os.Chdir(originalWD)
	})

	cfg, err := Load()
	require.NoError(t, err)
	require.Equal(t, "info", cfg.LogLevel)
	require.Equal(t, "", cfg.OpenAI.BaseURL)
